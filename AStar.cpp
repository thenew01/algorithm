bool operator<(const PointEx& left, const PointEx& right)
{
	//if( left.F < right.F ) return true;
	//else return false;
	
	if( left.x == right.x && left.y == right.y ) return false;
	
	if( left.x == right.x && left.y < right.y ) return true;
	else if( left.x < right.x ) return true;
	else return false;
}

